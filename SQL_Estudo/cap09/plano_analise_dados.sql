# Criando um plano de analise de dados
#
# Um plano de Analise de dados nao e diferente
# de qualquer outro tipo de planejamento
#
# Definimos o objetivo, coletamos os dados, 
# escolhemos as ferramentas, aplicamos as tecnicas
# de analise e entregamos os resultados
#
# O plano de analise de dado pode ser elebadora 
# em conjunto com a equipe de ciencia de dados
# areas de negocio, cliente ou apenas pelo analista
# de dados
#
# Normalmente seguimos estas 5 etapas ao construir
# um plano de analise de dados
#
# 1. Definição do Objetivo
# Tudo. Tudo.Tudo começa com a definição do objetivo
#
# Embora seja possivel explorar os dados sem um 
# objetivo ( o que é comum em trabalhos academicos de 
# pesquisa), para fins comercias sempre definimos 
# o objetivo antes de começar qualquer trabalho de 
# analise
#
# O que queremos analisar?
# Quais perguntas de negocio devem ser respondidas?
# Quais problemas estamos tentando resolver?
#
# 2. Coleta e Extração de Dados
#
# Precisamos de matéria-prima, logo, precisamos
# coletar dados de fontes existentes ou extrair 
# dados de outras fontes conforme necessário.
#
# Nesta etapa o trabalho de um Engenheiro de Dados
# pode ser necessário.alter
#
# 3. Escolha das ferramentas
#
# Quais serão as ferramentas de trabalho?
#
# Depende do nível de análise que iremos realizar
# Análise mais básica e estruturais podem ser feitas
# com Linguagem SQL, porém análises mais avançadas
# e complexas vão requerer ferramentas apropriadas como 
# Python, SQL, SAS ou alguma outra opção 
#
# 4. Aplicação das Técnicas de Análise
#
# A aplicacação das técnicas de análise vai depenter
# exatamente da definição do objetivo
#
# Normalmente começamos esta etapa com uma análise
# exploratória dos dados, na sequência calculamos
# estatísticas descritivas, agregação e sumarização
# até as técnicas mais avançadas de estatística inferencial,
# mineração de dados ou mesmo Machine Learning.
#
# 5. Entrega dos Resultados
#
# Por fim entregamos os resultados.
# O formato da entrega dever ser definido no começo
# do Plano. Relatórios, gráficos e tabelas são os 
# métodos mais comuns de entrega dos resultados.
#
# Aqui é importante usar comunicação clara, orientada
# à audiência que receberá os resultados.

